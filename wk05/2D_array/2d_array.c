#include <stdio.h>


// flag[0][0] offset = 0
// flag[1][0] offset = 12
// flag[2][0] offset = 24
// flag[4][3] offset = 4 * 12 + 3

// flag[i][j] offset = (i * N_COLS + j) * 1
char flag[6][12] = {
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'},
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'},
    {'.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'},
    {'.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.', '.'},
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'},
    {'#', '#', '#', '#', '#', '.', '.', '#', '#', '#', '#', '#'}
};

int main(void) {
    for (int row = 0; row < 6; row++) {
        for (int col = 0; col < 12; col++) {
            char ch = flag[row][col];
            printf ("%c", ch);
        }
        printf ("\n");
    }
    return 0;
}